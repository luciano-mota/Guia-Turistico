            <div class="texto">
                <p>
                    O Teatro Arthur Azevedo localiza-se na cidade de São Luís, no estado do Maranhão, no Brasil.
                    A ideia da criação do teatro surgiu em 1815, por iniciativa de dois comerciantes portugueses, 
                    Eleutério Lopes da Silva Varela e Estevão Gonçalves Braga. Essa era a plena época áurea do ciclo 
                    do algodão, em que o Maranhão enriquecia com a exportação deste produto e a cidade necessitava de 
                    maior vida cultural. No projeto original, se estenderia até o Largo do Carmo, mas foi reduzido por 
                    um veto da Igreja, que considerou a obra antirreligiosa.
                </p><br>
                <p>
                    A construção começou em 1816 e no dia 1º de julho de 1817, após um ano de trabalho, foi inaugurado. 
                    Chamou-se inicialmente Teatro União, em homenagem à criação do Reino Unido de Portugal, Brasil e 
                    Algarves (1815), resultado da vinda da família real portuguesa ao Brasil. Este era o quarto teatro 
                    da história de São Luís, mas destacava-se pelo conforto e tamanho, com uma capacidade para 756 espectadores. 
                    Seu estilo neoclássico era também uma novidade para a época.
                </p><br>
                <p>
                    Em 1852 passou a chamar-se Teatro São Luiz e, na década de 1920, ganhou o nome atual em homenagem ao grande 
                    dramaturgo maranhense Artur de Azevedo (1855-1908).
                    Em 1854, o teatro serviria de berço para a atriz maranhense Apolônia Pinto (1854-1937), filha de uma atriz 
                    portuguesa que entrou em trabalho de parto em pleno teatro, no Camarim n° 1. Ela se tornaria uma grande atriz, 
                    aos doze anos já encantando plateias com a peça "A Cigana de Paris". Foi homenageada com duas placas no Camarim nº 1, 
                    um busto em bronze, e seus restos mortais estão guardados no teatro.
                </p>
