            <div class="texto">
                <p>
                    O Teatro João do Vale é um teatro localizado na cidade de São Luís, no Maranhão.
                    Funcionou como um antigo depósito de açúcar, até o barracão ser adquirido pelo Governo 
                    do Maranhão, na administração de Epitácio Cafeteira (1987-1990), durante a execução do 
                    Projeto Reviver, sendo denominado Teatro Canarinho.
                </p><br>
                <p>
                    Posteriormente, em 1995, o decreto estadual nº 14.424 criou o Espaço Cultural João do Vale, 
                    com o objetivo de manter as ações do Grupo de Trabalho de Ações Integradas de Cultura – GTAIC/Fundação 
                    Cultural do Maranhão (hoje SECMA).
                    Após reforma, recebeu o nome de Teatro João do Vale, em homenagem ao compositor maranhense já falecido. 
                    Foi inaugurado em dezembro de 2001, com a intenção de funcionar como laboratório para o Curso de Formação de Atores do Centro de Artes Cênicas do Maranhão/CACEM.
                </p><br>
                <p>
                    Desvinculado do CACEM em 2006, passou a ser um órgão da Secretaria de Estado da Cultura (SECMA).
                    Possui lotação de 400 lugares (307 platéia e 93 mezanino) e está localizado na Rua da Estrela, nº 283, 
                    na Praia Grande, entre as Praças Nauro Machado e Valdelino Cécio e o Beco da Pacotilha.
                </p>
